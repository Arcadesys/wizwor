import { writeFile } from "node:fs/promises";
import { JSDOM } from "jsdom";
import { collectMatchingTags, matchingRules, matchesAny, slugify, stableStringify, unique } from "./heuristics.mjs";

export async function generateConsoleCatalog(config) {
  const generatedAt = new Date().toISOString();
  const documents = new Map();
  const byId = new Map();

  for (const tableSource of config.sourceTables) {
    if (!documents.has(tableSource.url)) {
      const html = await fetch(tableSource.url, {
        headers: {
          "User-Agent": "wizwor-catalog-generator/1.0 (local development script)",
        },
      }).then((response) => {
        if (!response.ok) {
          throw new Error(`Failed to fetch source ${tableSource.url}: ${response.status} ${response.statusText}`);
        }
        return response.text();
      });
      documents.set(tableSource.url, new JSDOM(html).window.document);
    }

    const document = documents.get(tableSource.url);
    const tables = [...document.querySelectorAll("table.wikitable")];
    const table = tables[tableSource.index];
    if (!table) {
      throw new Error(`Expected source table ${tableSource.index} for ${tableSource.category}`);
    }

    for (const row of [...table.querySelectorAll("tr")].slice(1)) {
      const entry = parseRow(row, tableSource, config);
      if (!entry) {
        continue;
      }

      let id = slugify(entry.title);
      if (byId.has(id)) {
        const baseId = slugify(`${entry.title}-${entry.sourceCategory}-${entry.publisher.join("-") || entry.year}`);
        id = baseId;
        let counter = 2;
        while (byId.has(id)) {
          id = `${baseId}-${counter}`;
          counter++;
        }
      }
      byId.set(id, { id, ...deriveGameFields(entry, config), ...entry, generatedAt });
    }
  }

  const entries = [...byId.values()].sort((left, right) => left.title.localeCompare(right.title));
  const sourceCounts = Object.fromEntries(
    config.sourceTables.map(({ category }) => [category, entries.filter((entry) => entry.sourceCategory === category).length]),
  );
  const regionLabels = config.regionLabels;
  const sourceCategoryUnion = config.sourceTables.map(({ category }) => `"${category}"`).join(" | ");

  const output = `// Generated by ${config.scriptName}. Do not edit by hand.
// Sources:
${config.sources.map((source) => `// - ${source.url}`).join("\n")}
// Retrieved: ${generatedAt}

import type { Game } from "./games";

type GeneratedGameMetadata = Omit<Game, "playthroughUrl">;

export type ${config.typePrefix}CatalogRegion = keyof typeof regionLabels;
export type ${config.typePrefix}CatalogSourceCategory = ${sourceCategoryUnion};

export type ${config.typePrefix}CatalogGame = GeneratedGameMetadata & {
  sourceCategory: ${config.typePrefix}CatalogSourceCategory;
  developer: string[];
  publisher: string[];
  regions: string[];
  firstReleased: string;
  format?: string;
  sourceUrl: string;
  generatedAt: string;
  signalScore: number;
};

export const regionLabels = ${stableStringify(regionLabels)} as const;

export const ${config.sourceExportName} = ${stableStringify({
    name: config.sourceName,
    retrievedAt: generatedAt,
    rowCount: entries.length,
    sources: config.sources,
    sourceCounts,
  })} as const;

export const ${config.gamesExportName}: ${config.typePrefix}CatalogGame[] = ${stableStringify(entries)};
`;

  await writeFile(config.outputPath, output);
  console.log(`Generated ${entries.length} ${config.platformLabel} catalog entries at ${config.outputPath.pathname}`);
  console.log(sourceCounts);
}

function parseRow(row, tableSource, config) {
  const cells = [...row.children].filter((cell) => cell.tagName === "TD" || cell.tagName === "TH");
  if (!cells.length || cells.length <= tableSource.minCells) {
    return null;
  }

  const title = extractTitle(cells[tableSource.columns.title]);
  const dateValues = tableSource.columns.dates?.map(({ index }) => cleanCell(cells[index])) ?? [];
  const firstReleased = tableSource.columns.date == null
    ? firstKnownDate(dateValues)
    : cleanCell(cells[tableSource.columns.date]);
  const regions = tableSource.columns.dates
    ? releaseRegions(Object.fromEntries(tableSource.columns.dates.map(({ region, index }) => [region, cleanCell(cells[index])])))
    : extractRegions(cells, tableSource);
  const format = tableSource.columns.format == null ? undefined : cleanCell(cells[tableSource.columns.format]);

  return normalizeEntry(
    {
      title,
      developer: readNames(cells, tableSource.columns.developer),
      publisher: readNames(cells, tableSource.columns.publisher),
      firstReleased,
      year: extractYear(firstReleased),
      regions,
      format,
      sourceCategory: tableSource.category,
      sourceUrl: tableSource.url,
    },
    config,
  );
}

function readNames(cells, index) {
  if (index == null || !cells[index]) {
    return [];
  }
  return extractNames(cells[index]);
}

function extractRegions(cells, tableSource) {
  const regions = [...(tableSource.constantRegions ?? [])];
  const regionIndex = tableSource.columns.regions;
  if (regionIndex != null && cells[regionIndex]) {
    regions.push(...cleanCell(cells[regionIndex]).split(/\s*,\s*|\s*\/\s*/).map((value) => value.trim()));
  }
  return unique(regions.filter(Boolean));
}

function normalizeEntry(entry, config) {
  const title = cleanTitle(entry.title);
  if (!title || /^title/i.test(title) || config.skipTitlePatterns?.some((pattern) => pattern.test(title))) {
    return null;
  }
  return {
    ...entry,
    title,
    developer: unique(entry.developer.map(cleanTitle).filter(Boolean)),
    publisher: unique(entry.publisher.map(cleanTitle).filter(Boolean)),
    year: entry.year || "Unknown",
    firstReleased: entry.firstReleased || entry.year || "Unknown",
    regions: unique(entry.regions.filter(Boolean)),
    platform: config.platform,
    sourceUrl: entry.sourceUrl,
  };
}

function deriveGameFields(entry, config) {
  const haystack = [entry.title, ...entry.publisher, ...entry.developer, entry.sourceCategory, entry.format ?? ""]
    .join(" ")
    .toLowerCase();
  const tags = new Set(collectMatchingTags(haystack, config.tagRules));

  for (const [category, categoryTags] of Object.entries(config.categoryTags ?? {})) {
    if (entry.sourceCategory === category) {
      for (const tag of categoryTags) {
        tags.add(tag);
      }
    }
  }
  if (entry.format) {
    tags.add(entry.format);
  }
  if (entry.regions.length === 1) {
    tags.add(`${entry.regions[0]} exclusive`);
  }

  const playStyleRule = config.playStyleRules.find((rule) => matchesAny(haystack, rule.patterns));
  const playStyle = playStyleRule?.playStyle ?? "side-scroller";
  const playStyleSignal = Boolean(playStyleRule);
  for (const tag of playStyleRule?.tags ?? ["action"]) {
    tags.add(tag);
  }

  const matchedMoodRules = matchingRules(haystack, config.moodRules);
  const moodSignal = matchedMoodRules.length > 0;
  const moods = matchedMoodRules.map((rule) => rule.mood);
  if (!moods.length) {
    moods.push(playStyle === "puzzle" ? "contemplative" : playStyle === "action-adventure" ? "heroic" : "arcade");
  }
  if (moods.length === 1) {
    moods.push(moods[0] === "arcade" ? "heroic" : "arcade");
  }

  const difficultSignal = matchesAny(haystack, config.difficultSignals);
  const casualSignal = matchesAny(haystack, config.casualSignals);
  const difficulty = difficultSignal ? "difficult" : casualSignal ? "casual" : "fair";
  const difficultySignal = difficultSignal || casualSignal;

  const richStorySignal = matchesAny(haystack, config.richStorySignals);
  const someStorySignal = matchesAny(haystack, config.someStorySignals);
  const story = richStorySignal ? "rich" : someStorySignal || playStyle === "action-adventure" ? "some" : "low";
  const storySignal = richStorySignal || someStorySignal;

  const obscurity = config.classicTitles.has(entry.title)
    ? "classic"
    : config.strangeCategories?.includes(entry.sourceCategory) || entry.regions.length === 1
      ? "strange"
      : "hidden-gem";

  const signalScore = [moodSignal, playStyleSignal, difficultySignal, storySignal].filter(Boolean).length;

  return {
    pitch: buildPitch(entry, config, playStyle, difficulty, story),
    moods: unique(moods).slice(0, 2),
    difficulty,
    story,
    playStyle,
    obscurity,
    isRomhack: false,
    signalScore,
    tags: [...tags].sort(),
  };
}

function buildPitch(entry, config, playStyle, difficulty, story) {
  const category = config.categoryPhrases?.[entry.sourceCategory] ?? `a ${config.platformLabel} release`;
  const feel = difficulty === "difficult" ? "demanding" : difficulty === "casual" ? "approachable" : "balanced";
  const narrative = story === "rich" ? "story-forward" : story === "some" ? "lightly narrative" : "play-first";
  return `${entry.title} is ${category} from ${entry.year}, tagged as ${feel}, ${narrative}, and ${playStyle.replace("-", " ")}-leaning for the recommender.`;
}

function releaseRegions(dates) {
  return Object.entries(dates)
    .filter(([, date]) => date && !/unreleased/i.test(date))
    .map(([region]) => region);
}

function firstKnownDate(dates) {
  return dates.find((date) => date && !/unreleased/i.test(date)) ?? "Unknown";
}

function extractTitle(cell) {
  const firstLink = [...cell.querySelectorAll("a")].find((link) => cleanCell(link));
  return cleanCell(firstLink ?? cell).split(/\n/)[0];
}

function extractNames(cell) {
  const links = [...cell.querySelectorAll("a")].map((link) => cleanCell(link)).filter(Boolean);
  if (links.length) {
    return links;
  }
  return cleanCell(cell)
    .split(/\s{2,}|\/|,/)
    .map((value) => value.trim())
    .filter(Boolean);
}

function cleanCell(cell) {
  const clone = cell.cloneNode(true);
  for (const sup of clone.querySelectorAll("sup")) {
    sup.remove();
  }
  return clone.textContent.replace(/\u00a0/g, " ").replace(/\s+/g, " ").trim();
}

function cleanTitle(value) {
  return value
    .replace(/\[[^\]]+\]/g, "")
    .replace(/•.+$/g, "")
    .replace(/\b(JP|NA|PAL|EU|BR|KR|AU)$/g, "")
    .replace(/\s*\([^)]+version\)\s*/gi, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function extractYear(value) {
  const match = value.match(/\b(19|20)\d{2}\b/);
  return match?.[0] ?? "Unknown";
}
